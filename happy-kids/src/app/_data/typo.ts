/* Типографика: не оставляем короткие предлоги/союзы в конце строки.
   Пробел после них заменяем на неразрывный (U+00A0) — слово переносится вместе
   со следующим. Работает только для кириллицы (английский не трогаем). */

const NBSP = String.fromCharCode(160); // U+00A0

export function typo(s: string): string {
  return s
    // 1–2-буквенные слова: и, в, во, на, с, к, по, о, у, не, а, но, из, от, до, за, об, же, бы, ли…
    .replace(/(?<=^|[\s(«„])([А-Яа-яЁё]{1,2})[ \t]+/g, (_m, w) => w + NBSP)
    // частые 3+ буквенные предлоги/союзы
    .replace(/(?<=^|[\s(«„])(что|как|или|для|при|без|под|над|про|чтобы|если|когда|через|между|перед|после)[ \t]+/gi, (_m, w) => w + NBSP);
}
